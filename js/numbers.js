function addition(firstNumber, secondNumber) {
    return firstNumber + secondNumber;
}

function substraction(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
}

function multiplication(firstNumber, secondNumber) {
    return firstNumber * secondNumber;
}

function division(firstNumber, secondNumber) {
    return firstNumber / secondNumber;
}

function exponientation(firstNumber, secondNumber) {
    return Math.pow(firstNumber, secondNumber);
}

function getRemainder(firstNumber, secondNumber) {
    return firstNumber % secondNumber;
}

function squareRoot(number) {
    return Math.sqrt(number);
}

function absoluteValue(number) {
    return Math.abs(number)
}

function roundNumber(decimalNumber) {
    return Math.round(decimalNumber)
}

function generateRandomNumber() {
    return Math.round(Math.random());
}

